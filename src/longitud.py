
def inch_cm (number):
    result = number * 2.54
    return result


def cm_inch (number):
    result = number * 0.393701
    return result

def km_meters (number):
    result = number * 1000
    return result

def meters_km (number):
    result = number / 1000
    return result

def yard_meter (number):
    result = number * 0.9144
    return result

def meter_yard (number):
    result = number * 1.09361
    return result